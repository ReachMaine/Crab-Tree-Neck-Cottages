	<p><div class="left"><img src="images/photos/text/03.jpg" alt="" /><br />Casita Blackberry </div>Nuestras dos casitas acogedoras, hechas de pino rústico, están rodeadas por el bosque y los campos de Hancock Point (la península de la punta de Hancock) <strong>La Casita Blackberry</strong> está escondida debajo de los árboles y la <strong>Casita Blueberry</strong> tiene vista a la pradera y al campo de arándanos. Se llega a la costa fácilmente caminando. Estamos ubicados en Hancock, Maine, a mitad de camino entre Acadia National Park, ubicado en Mount Desert Island, y Acadia National Park, en Schoodic Point. Esta ubicación aislada está cerca de la aldea de Hancock, en un área excelente para realizar caminatas, practicar ciclismo, kayaking o recolectar frutos rojos. Coloque un pie afuera y realice una caminata pacífica de diez minutos a Jellison Cove en Frenchman's Bay o explore los bosques y campos cercanos.<br /><br /><div style="color: #003300; text-align: right;">Avanzar hacía abajo para ver más <a href="es.html#fotos">fotos</a></div></p>

	<p class="clear"><div class="right"><img src="images/photos/text/23.jpg" alt="" /><br />Casita Blueberry</div>Las casitas son nuevas y están totalmente amuebladas. Cuentan con una cocina simple y completa, una combinación cómoda de living y comedor, 2 habitaciones en la planta inferior (una con una cama de dos plazas y otra con 2 literas). Además, cuenta con un gran loft para dormir en la planta superior (con una cama del tamaño de la reina y una cama gemela y televisor). Aunque ambas casitas albergan hasta siete personas y cuentan con mesas grandes para picnic en las galerías cubiertas, la Casita Blueberry tiene una galería más amplia y tiene un poco más de espacio en la cocina comedor; por lo tanto, se hace más fácil acomodar a grupos más grandes para cenar en el interior. Se puede disponer de una cuna, si se la solicita. Las casas están equipadas con parrillas a gas en las galerías.</p>
	
	<p class="clear"><img src="images/photos/text/39.jpg" alt="" class="left" />Le ofreceremos radio, teléfono para llamadas locales, televisor y reproductor de DVD para mirar películas, reloj despertador, toda la blanquería de cama y toallas, parrilla a gas, lavadora y secadora, plancha y tabla de planchar. Hay una estufa a leña tipo hogar y se provee la leña.</p>

	<p>Disfrute la belleza de la costa de Maine. Pasee en bicicleta hasta la Reserva Natural de Frenchman Bay, donde puede caminar, practicar kayak o hacer picnic. Dé un paseo por Hancock Point y vea las maravillosas casas antiguas históricas, visite la biblioteca y la Playa Carter, todo aquí en Hancock Point.</p>

	<p class="clear"><img src="images/photos/text/15.jpg" alt="" class="right" />Dé un paseo en automóvil hacia el Parque Nacional Acadia, ubicado en Mount Desert Island (a 40 minutos), o el Parque Nacional Acadia, en Schoodic Point (a 25 minutos aproximadamente, pero en la dirección opuesta) y disfrute las aldeas costeras por excelencia de Maine, a lo largo de todo el camino. Vaya de compras o al cine en Bar Harbor (a 45 minutos) o Ellsworth (a 20 minutos).</p>

	<p>El pequeño pueblo de Hancock, Maine se enorgullece de algunos restaurantes fabulosos y finos (Crocker House, Le Domaine) y de varios otros restaurantes modestos, con ofertas locales y étnicas maravillosas. O simplemente coma en las galerías cubiertas de nuestras casitas y disfrute de la noche pacífica, observando el atardecer y escuchando el canto de los pájaros.</p>
	
	<p>Recolecte las moras y arándanos de Maine, que se encuentran justo afuera de la puerta de su casita, durante los meses de julio y agosto. Hay lugares cercanos para el arriendo de bicicletas, kayaks/canoas y tours guiados. Para nadar, pasear en bote, caminar y escalar montañas, visite las tierras de la Reserva Pública de Maine en las lagunas cristalinas de los lagos Donnell y Flanders. Hay canchas de tenis y lugares para tomar lecciones de navegación no muy lejos de las casitas; se debe organizar la actividad con anterioridad. También se dispone de lugares para cabalgar y tomar lecciones, una playa pública y una biblioteca de verano pintoresca; todos estos lugares son de fácil acceso en bicicleta.</p>

